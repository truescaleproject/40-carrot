// Build Map
const buildBaseSizeMap = () => {
    const map = new Map<string, string>();
    const lines = RAW_BASE_SIZE_DATA.split('\n');
    const splitRegex = /^(.+?)\s+(Hull|Unique|Small Flying Base|Large Flying Base|\d+mm|\d+x\d+(?:\.\d+)?mm(?:.*)?)$/i;

    for (const line of lines) {
        const trimmed = line.trim();
        if (!trimmed) continue;
        if (/^[A-Z\s’']+$/.test(trimmed)) continue; // Skip Header Lines like "ADEPTA SORORITAS"

        const match = trimmed.match(splitRegex);
        if (match) {
            const name = match[1].trim().toLowerCase();
            const size = match[2].trim();
            // Store by name
            map.set(name, size);
            
            // Also store stripped version (no special chars)
            const stripped = name.replace(/[^a-z0-9]/g, '');
            if (stripped !== name.replace(/\s/g, '')) {
                map.set(stripped, size);
            }
        }
    }
    return map;
};

const BASE_SIZE_MAP = buildBaseSizeMap();

export const getBaseSize = (unitName: string): string | undefined => {
    if (!unitName) return undefined;
    const norm = unitName.toLowerCase().trim();
    
    // Direct match
    if (BASE_SIZE_MAP.has(norm)) return BASE_SIZE_MAP.get(norm);
    
    // Stripped match
    const stripped = norm.replace(/[^a-z0-9]/g, '');
    if (BASE_SIZE_MAP.has(stripped)) return BASE_SIZE_MAP.get(stripped);

    // Partial match for complicated names (e.g. "Squad: Leader")
    // Try to split by colon
    if (norm.includes(':')) {
        const parts = norm.split(':');
        // Try the last part (often the specific model name)
        const lastPart = parts[parts.length - 1].trim();
        if (BASE_SIZE_MAP.has(lastPart)) return BASE_SIZE_MAP.get(lastPart);
    }

    return undefined;
};
